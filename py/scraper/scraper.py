from jobspy import scrape_jobs

def scrape_jobs(site_name, search_term, results_wanted, country_indeed, hours_old, distance, is_remote):
    return scrape_jobs(
        site_name=site_name,
        search_term=search_term,
        results_wanted=results_wanted,
        country_indeed=country_indeed,
        hours_old=hours_old,
        distance=distance,
        is_remote=is_remote,
    )
